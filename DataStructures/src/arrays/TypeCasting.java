package arrays;

public class TypeCasting {

	public static void main(String[] args) {
		 double a= 295.04;
	        int b = 300;
	        
	        int e = (int)a;
	        byte f = (byte)e;
	        byte c = (byte)a;
	        byte d = (byte)b;
	        
	        System.out.println(c + " "+ d+ "  "+e+ "  "+f);
	}

}
